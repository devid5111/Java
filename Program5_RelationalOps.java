public class Program5_RelationalOps {
    public static void main(String[] args) {
        int x = 5;
        int y = 8;

        System.out.println("x == y is " + (x == y)); // Equal to
        System.out.println("x != y is " + (x != y)); // Not equal to
        System.out.println("x > y  is " + (x > y));  // Greater than
        System.out.println("x < y  is " + (x < y));  // Less than
        System.out.println("x >= 5 is " + (x >= 5)); // Greater than or equal to
        System.out.println("y <= 8 is " + (y <= 8)); // Less than or equal to
    }
}
